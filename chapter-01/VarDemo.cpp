// using an operator 

#include <iostream>
using namespace std;

int main()
{
    int length; // this declares the variable
    int width; // this declares another variable
    int area; // this does, too 

    length = 7;  // this assign 7 to length 
    width = 5;  // this assigns 5 to width 
    area = length * width; // Compute area 

    cout << "This area is ";
    cout << area; // This displays 35 

    return 0; 

}